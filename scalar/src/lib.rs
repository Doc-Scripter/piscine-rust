
#[allow(dead_code)]
pub fn sum(a: u8, b: u8) -> u8 {
    a + b
}
#[allow(dead_code)]
pub fn diff(a: i16, b: i16) -> i16 {
    a - b
}

#[allow(dead_code)]
pub fn pro(a: i8, b: i8) -> i8 {
    a * b
}

#[allow(dead_code)]
pub fn quo(a: f64, b: f64) -> f64 {
    a / b
}

#[allow(dead_code)]
pub fn rem(a: f64,b:f64)->f64 {
    a%b
}
